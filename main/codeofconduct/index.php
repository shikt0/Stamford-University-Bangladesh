        <strong>The following activities would be treated as committing major offenses:</strong>

        <ul>
            <li>Any activity that impedes, hinders, prevents the right of a student to attend classes.</li>
            <li>Any act that disturbs the peace and order either in class room or in the campus.</li>
            <li>Carrying any sort of deadly or dangerous weapon or explosive of any kind inside the university.</li>
            <li>Possessing or taking any sort of drug or seductive product.</li>
            <li>Bringing intoxicating drinks and /or regulated drugs inside the campus.</li>
            <li>Willful disregard of authority, disrespect, discourtesy and disobedience to any of the faculty, officials and stuff of the university.</li>
            <li>Any sort of physical assault and/or injuries to any one of the university.</li>
            <li>Possession or distribution of any sort of pornographic materials.</li>
            <li>Stealing any material of the university.</li>
            <li>Extortion or unauthorized solicitation</li>
            <li>Selling tickets, raffles or lotteries without permission of the university authority.</li>
            <li>Using disrespectful or indecent language and committing offensive acts.</li>
            <li>Any activity leading to defamation and maligning.</li>
            <li>Destruction of university property or properties belonging to any member of the faculty and non-teaching staff</li>
            <li>Any sort of forgery or falsification and attempt thereof.</li>
            <li>Organizing gambling or playing cards inside the class room or campus.</li>
            <li>Any activity that leads to misrepresentation and dishonesty.</li>
            <li>Entering the class room and/or offices without proper permission.</li>
            <li>Planning and organizing activities contrary to the best interest of the university.</li>
            <li>Violating any clauses of the university circulation/notice.</li>
            <li>Printing of any document/material using the names and seals of the university.</li>
        </ul>
    </details>


