        <p>A student is expected to attend all the classes in each course. Maximum 20% absence in a course in one trimester may be exempted under emergency situation. It is the responsibility of the student to keep the course teacher informed regarding absence from classes. It would be the responsibility of the course teacher to announce the attendance policy at the beginning of the trimester. The course teacher will determine the manner by which, assignments or examinations missed by students, may be made up.</p>
