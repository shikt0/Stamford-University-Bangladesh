    <details>
        <summary>Dress Code for the Students</summary>
        <p>Every student of Stamford is required to dress himself/herself decently and properly for being a proud student of Stamford University. Students are advised not to enter the university campus or classroom in the following attire:</p>

        <ul>
            <li>Without properly pinned ID card.</li>
            <li>Wearing a sleeveless, hanging t-shirt or chemise.</li>
            <li>Wearing a wrack, ragged or slashed jeans/trousers.</li>
            <li>Wearing unclean clothes.</li>
            <li>Wearing half-pant or skirt.</li>
            <li>Putting on slippers of ordinary type.</li>
            <li>Wearing provocative and seductive clothing/dress.</li>
        </ul>
    </details>


    <details>
        <summary>Library Code</summary>
        <strong>Students are advised to strictly observe the following code of conduct while using the library facility of the university:</strong>

        <ul>
            <li>Students must bear the library card while in the library.</li>
            <li>Folder, files and bags etc. are not allowed in the library.</li>
            <li>Students are permitted to take 02(two) different books at a time for ten days only. On expiry due date Tk.10/ only will be charged per day for each book.</li>
            <li>Library remains open from <strong>9 a.m. to 8 p.m.</strong> except on holidays.</li>
            <li>Students must return books on due date even if there is no class held on that day.</li>
            <li>Library users must observe silence. All sorts of non academic discussions and unnecessary noise are strictly prohibited.</li>
            <li>Smoking, eating and drinking (except fresh water) are strictly prohibited in the library.</li>
            <li>In case of loss of the Library card a student has to deposit Tk. 50.00 only (for each) for a new one.</li>
            <li>Library members may have their library card reissued against deposit (the damaged/old library card) for once only without any charge.</li>
            <li>In case of loss of a book a student must replace the book by a new one or pay three times of the price of the book along with delay fine.</li>
        </ul>

        <p>For any kind of misconduct /violation of library rules and regulations there is a provision for holding up the library card/expulsion from membership. Students are to keep their personal belongings against a token. They are also requested not to keep any valuable materials (Cell phone, money, ID card etc.) in their bags. Library authority will not be responsible for missing any such items. If any student loses his/her token he/she must pay Tk. 100 only as fine. Students shall be held responsible for making any damage, page cutting or marking in the book; in such case he/she will have to replace the new copy. If a library book is found with a student outside the library without being issued, the student will be treated as a miscreant. In such a malpractice, the following penalty will be imposed on the concerned student:</p>

        <ul>
            <li>Library membership for one/more trimester will be held up.</li>
            <li>Tk, 5000 to be realized as fine.</li>
            <li>Examination for one trimester will be held up.</li>
            <li>Result will remain withheld until fine amount is realized.</li>
        </ul>
    </details>



